public class ConstructingStrings {

    public static void main(String[] args) {
        String s1;
        String s2 = "";
        String s3 = null;
        String s4 = new String();
        
        StdOut.println(s1);
        StdOut.println(s2);
        StdOut.println(s3);
        StdOut.println(s4);

    }
    
}
